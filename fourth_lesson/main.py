import matplotlib.pyplot as plt

#שאלה 1
# נתוני טמפרטורות ממוצעות לכל חודש
months = ['ינואר', 'פברואר', 'מרץ', 'אפריל', 'מאי', 'יוני', 'יולי', 'אוגוסט', 'ספטמבר', 'אוקטובר', 'נובמבר', 'דצמבר']
temperatures = [5, 6, 10, 15, 20, 25, 30, 29, 24, 18, 10, 6]  # טמפרטורות ממוצעות לדוגמה

# יצירת גרף קו
plt.plot(months, temperatures)

# הוספת כותרות
plt.title('שינוי בטמפרטורה לאורך זמן')
plt.xlabel('חודש')
plt.ylabel('טמפרטורה (°C)')

# הצגת הגרף
plt.grid()
plt.show()

